#include "quicksort.h"

void quickSort(int **in, int sz){
    quickSort_(*in,0,sz-1);
}
void quickSort_(int *arr, int left, int right) {
      int i = left, j = right;
      int tmp;
      int pivot = arr[(left + right) / 2];
 
      /* partition */
      while (i <= j) {
            while (arr[i] < pivot)
                  i++;
            while (arr[j] > pivot)
                  j--;
            if (i <= j) {
                  tmp = arr[i];
                  arr[i] = arr[j];
                  arr[j] = tmp;
                  i++;
                  j--;
            }
      }
 
      /* recursion */
      if (left < j)
            quickSort_(arr, left, j);
      if (i < right)
            quickSort_(arr, i, right);
}
